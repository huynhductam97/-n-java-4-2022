package DTO;

public class KhachHangDTO {

  private String maKhachHang;
  private String hoKhachHang;
  private String tenKhachHang;
  private String chungMinhNhanDan;
  private String soDienThoai;


  public String getMaKhachHang() {
    return maKhachHang;
  }

  public void setMaKhachHang(String maKhachHang) {
    this.maKhachHang = maKhachHang;
  }

  public String getHoKhachHang() {
    return hoKhachHang;
  }

  public void setHoKhachHang(String hoKhachHang) {
    this.hoKhachHang = hoKhachHang;
  }

  public String getTenKhachHang() {
    return tenKhachHang;
  }

  public void setTenKhachHang(String tenKhachHang) {
    this.tenKhachHang = tenKhachHang;
  }

  public String getChungMinhNhanDan() {
    return chungMinhNhanDan;
  }

  public void setChungMinhNhanDan(String chungMinhNhanDan) {
    this.chungMinhNhanDan = chungMinhNhanDan;
  }

  public String getSoDienThoai() {
    return soDienThoai;
  }

  public void setSoDienThoai(String soDienThoai) {
    this.soDienThoai = soDienThoai;
  }
}
